void LoadBunny(std::vector<Triangle>& tris) {
	std::vector<Triangle> triangles;
	std::ifstream infile("Resources/bunny.obj");
	std::string line;

	vec3 white(  0.75f, 0.75f, 0.75f );

	/* Vector to store all vertices */
	vector<vec4> vs;

	while (std::getline(infile, line)) {

		/* Try parse line */
		std::istringstream iss(line);
		string code;
		float a, b, c;
		if (!(iss >> code >> a >> b >> c)) {
			cout << "Could not read file" << endl;
			break;
		}

		if (code.compare("v") == 0) {
			/* Then this is a vertex definition */
			vec4 vertex = vec4(a, b, c, 1.0);
			vs.push_back(vertex);
		} else {
			/* This is a face definition */
			Triangle triangle = Triangle(vs[a-1], vs[b-1], vs[c-1], white);
			triangles.push_back(triangle);
		}
	}

	mat3 rotation;
	GetRotationMatrix(0, M_PI, 0, rotation);
	rotate(triangles, rotation);

	float L = 0.5f;
	scale(triangles, L);
	translate(triangles, 2, vec3(-0.3, -0.22, 0.35));

	for( size_t i=0; i<triangles.size(); ++i ) {
		tris.push_back(triangles[i]);
	}

}

